#商品类型
CREATE TABLE shop_type(
st_id INT PRIMARY KEY AUTO_INCREMENT,
st_name VARCHAR(50)
)
#商品
CREATE TABLE shop(
s_id INT PRIMARY KEY AUTO_INCREMENT,
s_name VARCHAR(50),
s_intro VARCHAR(200),
s_img VARCHAR(30),
s_color VARCHAR(20),
s_price DOUBLE,     
