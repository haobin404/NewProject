sv_id INT PRIMARY KEY AUTO_INCREMENT,
sv_name VARCHAR(50),
sv_price DOUBLE,
s_id INT,
CONSTRAINT FOREIGN KEY (s_id) REFERENCES shop(s_id) 
)

